# ==========================================================
# OpenShell Console
# Service Registry
# ==========================================================


class ServiceRegistry:

    def __init__(self):

        self._services = {}

    # ======================================================
    # REGISTER
    # ======================================================

    def register(
        self,
        name,
        service
    ):

        self._services[name] = service

    # ======================================================
    # GET
    # ======================================================

    def get(
        self,
        name
    ):

        return self._services.get(name)

    # ======================================================
    # EXISTS
    # ======================================================

    def exists(
        self,
        name
    ):

        return name in self._services

    # ======================================================
    # LIST
    # ======================================================

    def list(self):

        return list(
            self._services.keys()
        )